package RoadMapStep2.topic2;

public class SwapValues {
    public static void main(String[] args) {

        int firstValue = 143;
        int secondValue = 88;
        System.out.println("Значение до обмена: " + firstValue + ", " + secondValue);

        firstValue = 88;
        secondValue = 143;
        System.out.println("Значение после обмена: " + firstValue + ", " + secondValue);
    }
}
