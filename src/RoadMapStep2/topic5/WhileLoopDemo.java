package RoadMapStep2.topic5;

/**
 * Использует цикл while для вывода чисел от 1 до 1000.
 * Каждое число выводится на новой строке.
 */

public class WhileLoopDemo {
    public static void main(String[] args) {
        int i = 0;
        while (i < 1000) {
            i++;
            System.out.println(i);
        }
    }
}